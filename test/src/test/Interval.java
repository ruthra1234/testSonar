package ca.homedepot.util;

import java.util.Date;


/**
 * The Class Interval.
 *
 * @author RXP8RN9
 */
public class Interval
{

	/** The end. */
	public Date start, end;

	/**
	 * Instantiates a new interval.
	 *
	 * @param start the start
	 * @param end the end
	 */
	public Interval(final Date start, final Date end)
	{
		this.start = start;
		this.end = end;
	}

}
